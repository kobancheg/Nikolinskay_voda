        <p><a id="nalozh" name="nalozh"></a> <span class="size16bold">6. Оплата наложенным платежом на почте</span></p>
        <hr/>
        <p>Этот способ оплаты позволяет Вам не делать предоплату за товар, а оплатить при его получении на почте.
            Обращаем ваше внимание, что это один из самых затратных способов оплаты. </p>

        <p>Сумма наложенного платежа складывается из следующих составляющих: </p>

        <ul>
            <li><i>Заявленная стоимость заказа</i> (то есть стоимость заказанных вами товаров).</li>

            <li><i>Почтовый тариф</i>, зависящий от веса и расстояния. В случае больших габаритов посылки сверх
                почтового тарифа взымается дополнительная наценка 30%.
            </li>

            <li><i>Страховой сбор</i> 4% от <b>объявленной ценности</b>, которая <b>равна сумме наложенного платежа</b>.
                Это обязательный сбор, от которого по правилам почты невозможно отказаться. 
            </li>

            <li><i>Предпочтовая подготовка (упаковка и отправка)</i>. Стоимость составляет 70 рублей для посылки
                стандартных габаритов и 2 х 70 = 140 рублей (двойной тариф) для крупногабаритной посылки (упаковка и
                перевозка крупногабаритных посылок всегда сопряжена с дополнительными затратами). Стандартными
                габаритами по <a
                        href="http://www.russianpost.ru/rp/servise/ru/home/postuslug/bookpostandparcel/parcelltariff"
                        target="_blank">правилам Почты России</a> считаются габариты, не превышающие <strong>
                    425х265х380 мм.</strong></li>

            <li><strong>К</strong><span style="font-weight: bold;">омиссия за пересылку почтового перевода</span><span
                        style="font-weight: bold;"></span>. Процент комиссии составляет 3,5% от объявленной ценности.
                Данную комиссию оплачивает любой, кто делает почтовый перевод в отделении Почты России. При оплате
                наложенного платежа за свой заказ Вы также делаете почтовый перевод на наш расчётный счёт.
                <br/>
            </li>
        </ul>
        <b>ВНИМАНИЕ!</b> Сумма наложенного платежа, которая рассчитывается при оформлении заказа на нашем сайте,
        является ОКОНЧАТЕЛЬНОЙ. То есть вы <b>НЕ оплачиваете</b> в почтовом отделении никаких дополнительных сборов,
        потому что всё УЖЕ ВХОДИТ в сумму наложенного платежа. <b><span class="imp">Причем наша комиссия 3,5%, что меньше, чем у</span>
